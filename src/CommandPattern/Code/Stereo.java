package CommandPattern.Code;

public class Stereo {
    public void On() {
        System.out.println("Stereo On...");
    }

    public void Off() {
        System.out.println("Stereo Off...");
    }

    public void OpenLight() {
        System.out.println("Light is opened...");
    }

    public void CloseLight(){
        System.out.println("Light is closed...");
    }
}